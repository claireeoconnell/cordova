  <p>
    <strong>The Molecular Otolaryngology &amp; Renal Research Lab</strong> (MORL) Deafness Variation Database (DVD)
    provides a comprehensive guide to genetic variation in genes known to be associated with deafness. It includes
    all known genetic variants present in any gene that is included on 
    <a href="https://morl.lab.uiowa.edu/clinical-diagnostics/deafness-otoscope/otoscope-genetic-testing/genes-included-otoscope" title="Link to OtoSCOPE genes">OtoSCOPE&#174;</a>,
    the MORL's
    <a href="https://morl.lab.uiowa.edu/deafness" title="Link to OtoSCOPE">comprehensive genetic deafness screening platform</a>.
    This facilitates variant analysis.
  </p>

  <p>
    The data in the MORL DVD are based on publicly available data and data from OtoSCOPE&#174;. The DVD also incorporates
    phenotypic information from the
    <a href="http://audiogene.eng.uiowa.edu" title="Link to AudioGene">AudioGene machine-learning based audiometric profiling tool</a>.
  </p>

  <p>
    The goal of the DVD is to collate and annotate every variant in every deafness gene. Please take some time to review
    the <a href="sources">sources we use for annotation</a> as well as our <a href="classification">variant classification criteria</a>.
  </p>

  <p>
    The DVD is continually curated. This version represents our most recent interpretation of these data and supersedes all previous
    versions of the DVD and variant calling as reported in <a href="http://1.usa.gov/1DBNKu2">Shearer AE et al, 2014</a>.
    If you would like to add a gene or variant to the database, please <a href="javascript://" class="contact-popup" title="Contact Page">contact us</a>.
  </p>

  <p>
    These data are freely available. Please review our <a href="references">citation policy</a> when including information from the DVD in publication.
    All content is copyrighted by the MORL at The University of Iowa.
